#include "BasePower.h"


//-------------------------------------------------------------
//!
//! @brief コネクトウォーズ
//! @brief コネクトウォーズ関連の名前空間
//!
//-------------------------------------------------------------
namespace ConnectWars
{
    /*************************************************************//**
     *
     *  @brief  コンストラクタ
     *  @param  値
     *
     ****************************************************************/
    C_BasePower::C_BasePower(int32_t value) :

        // 値
        value_(value)

    {
    }


    /*************************************************************//**
     *
     *  @brief  デストラクタ
     *  @param  なし
     *
     ****************************************************************/
    C_BasePower::~C_BasePower()
    {
    }


    /*************************************************************//**
     *
     *  @brief  値を加える
     *  @param  値
     *  @return なし
     *
     ****************************************************************/
    void C_BasePower::Add(int32_t value)
    {
        value_ += value;
    }


    /*************************************************************//**
     *
     *  @brief  値を引く
     *  @param  値
     *  @return なし
     *
     ****************************************************************/
    void C_BasePower::Subtract(int32_t value)
    {
        value_ -= value;
    }


    /*************************************************************//**
     *
     *  @brief  値を取得する
     *  @param  なし
     *  @return 値
     *
     ****************************************************************/
    int32_t C_BasePower::GetValue() const
    {
        return value_;
    }


    /*************************************************************//**
     *
     *  @brief  値を設定する
     *  @param  値
     *  @return なし
     *
     ****************************************************************/
    void C_BasePower::SetValue(int32_t value)
    {
        value_ = value;
    }
}