#include "BasePower.h"


//-------------------------------------------------------------
//!
//! @brief コネクトウォーズ
//! @brief コネクトウォーズ関連の名前空間
//!
//-------------------------------------------------------------
namespace ConnectWars
{
    /*************************************************************//**
     *
     *  @brief  コンストラクタ
     *  @param  パワー
     *
     ****************************************************************/
    C_BasePower::C_BasePower(int32_t power) :

        // パワー
        power_(power)

    {
    }


    /*************************************************************//**
     *
     *  @brief  デストラクタ
     *  @param  なし
     *
     ****************************************************************/
    C_BasePower::~C_BasePower()
    {
    }


    /*************************************************************//**
     *
     *  @brief  パワーを加える
     *  @param  パワー
     *
     ****************************************************************/
    void C_BasePower::Add(int32_t power)
    {
        power_ += power;
    }


    /*************************************************************//**
     *
     *  @brief  パワーを取得する
     *  @param  なし
     *  @return パワー
     *
     ****************************************************************/
    int32_t C_BasePower::GetPower() const
    {
        return power_;
    }


    /*************************************************************//**
     *
     *  @brief  パワーを設定する
     *  @param  パワー
     *  @return なし
     *
     ****************************************************************/
    void C_BasePower::SetPower(int32_t power)
    {
        power_ = power;
    }
}